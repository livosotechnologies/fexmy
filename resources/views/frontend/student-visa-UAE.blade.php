                        <h5>UPCOMING INTAKES IN THE USA</h5>
                        <p style="text-align: justify";>US universities have 3 intakes.</p>
                        <p style="text-align: justify";>Students have the option of choosing the term of their study from
                            three main flexible intakes, Spring (January), Fall (September) and Summer (May) when they study
                            in USA.</p>
                        <p style="text-align: justify";><strong><b>Intake 1:</b></strong> Fall Semester – It commences in
                            August/September and is the major intake.</p>
                        <p style="text-align: justify";><strong><b>Intake 2:</b></strong> Spring Semester – It commences in
                            January/February intake is also available.</p>
                        <p style="text-align: justify";><strong><b>Intake 3:</b></strong> Summer Semester – It commences in
                            May/June and is available for selected Courses.</p>
                        <p style="text-align: justify";>You will have to choose an intake that best suits your requirements
                            and make your application accordingly. Remember the deadline to submit applications are usually
                            a few months before the starting date of the semester. You will also have to plan your US
                            student visa application to sync with your college application process.</p>
                        <h3 class="sec-title pt-3 pb-3">Eligibility requirements for students in US universities:</h3>
                        <p style="text-align: justify";><b>Student Applicant</b></p>

                        <li>Students must be over 18 years of age.</li>
                        <li>International students can work on-campus up to 20 hours/week or less during the academic terms
                            and full-time during the academic break periods including the summertime.</li>
                        <li>Off-campus employment requires some form of written or documented authorization issued by either
                            the USCIS or OISS.</li>
                        <li>You must be currently in legal status and have been enrolled as a student on a F-1 student visa
                            in the US for a minimum of one academic year to be eligible for any form of off-campus
                            employment.</li>

                        <h3 class="sec-title pt-3 pb-3">US Student Visa Requirements (F1 Visa)</h3>
                        <p style="text-align: justify";>You will generally need the following for your US Student Visa
                            application for US universities:</p>


                        <li>A valid passport with a validity date at least six months beyond your period of stay.</li>
                        <li>Recent passport size photograph.</li>
                        <li>Confirmation page of DS-160.</li>
                        <li>Form I -20.</li>
                        <li>Payment of application fees for SEVIS.</li>
                        <li>Application as a non-immigrant.</li>
                        <li>Your University will make you aware of additional requirements if any prior to your application.
                        </li>

                        <h3 class="sec-title pt-3 pb-3">STEPS TO APPLY</h3>
                        <p style="text-align: justify";><strong>Step 1: Apply to a US school and get accepted</strong></p>
                        <p style="text-align: justify";>The first step is to apply to (and eventually be accepted to) a US
                            university. In the United States, most full-time undergraduate and graduate programs demand
                            applications by December or January of each year. Admission notices are usually sent out in
                            March and April.</p>
                        <p style="text-align: justify";><strong>Step 2: Get your school&#8217;s Form I-20 or
                                DS-2019</strong></p>
                        <p style="text-align: justify";>After being accepted to a school, you&#8217;ll receive one of two
                            forms: Form I-20 (Certificate of Eligibility for Nonimmigrant Student Status) for F-1 and M-1
                            students, and Form DS-2019 (Certificate of Eligibility for Exchange Visitor (J-1) Status for J-1
                            students.</p>
                        <p style="text-align: justify";><strong>Step 3: Pay the SEVIS I-901 fee</strong></p>
                        <p style="text-align: justify";>Go online and pay the I-901 SEVIS fee once you receive your I-20 or
                            DS-2019 form from your school. The price is 350 USD for F-1/M-1 students and 220 USD for J-1
                            students once again. (Those in short-term J-1 visa programs will only have to pay $35 USD.)</p>
                        <p style="text-align: justify";><strong>Step 4: Locate a US Embassy or Consulate in Your
                                Area</strong></p>
                        <p style="text-align: justify";>You must apply for an international student visa at the US embassy
                            or consulate nearest to you (ideally, in the city or region in which you live). The US
                            Department of State offers an online search for US embassies and consulates.</p>
                        <p style="text-align: justify";>Please know that depending on which embassy you apply through, the
                            process for obtaining a student visa in the United States may fluctuate slightly.</p>
                        <p style="text-align: justify";><strong>Step 5: Fill out the DS-160 form online</strong></p>
                        <p style="text-align: justify";>After that, fill out the Online Non-immigrant Visa Application (Form
                            DS-160).</p>
                        <p style="text-align: justify";>You will also select the US embassy where you will interview for
                            your visa on this application.</p>
                        <p style="text-align: justify";><strong>Step 6: Make an appointment for your visa interview</strong>
                        </p>
                        <p style="text-align: justify";>Contact your nearest US embassy or consulate (preferably, the one
                            you listed on your online application) to organize your visa interview after you&#8217;ve filed
                            Form DS-160.</p>
                        <p style="text-align: justify";><strong>Step 7: Make payment for your visa application fee.</strong>
                        </p>
                        <p style="text-align: justify";>After that, pay the application fee of 160 USD. This charge is the
                            same regardless of where you apply or where you are from.</p>
                        <p style="text-align: justify";>It&#8217;s important to note that the exact date you pay this charge
                            will differ based on your embassy.</p>
                        <p style="text-align: justify";><strong>Step 8: Attend your visa interview</strong></p>
                        <p style="text-align: justify";>The interview is the final major step in the visa application
                            process. This interview will determine whether or not you are granted a student visa in the
                            United States.</p>
                        <p style="text-align: justify";>Step 9: Pay the visa issuance fee</p>
                        <p style="text-align: justify";>After being authorized for a student visa in the United States,
                            certain students are required to pay a visa issuance fee. Whether or not you must pay this fee
                            is determined by your nationality and the reciprocity agreement your country has with the United
                            States. You can use a chart on the US visas website to discover if you have to pay a visa
                            issuance fee.</p>
                        <h3 class="sec-title pt-3 pb-3">Step 10: Get Your Visa</h3>
                        <p style="text-align: justify";>Your embassy will return your passport to you with your new visa in
                            it if you&#8217;ve completed all of the requirements above and gained approval for an
                            international student visa to the United States. It&#8217;s worth noting that certain embassies
                            will ask you to pick it up in person, while others will simply ship it back to you.</p>
                        <p style="text-align: justify";>The length of time it takes for your visa to be processed varies by
                            embassy.</p>

                        <h3 class="sec-title pt-3 pb-3">After You Graduate:</h3>

                        <li>F1 Student Visa holders are eligible for up to 12 months of OPT (optional practical training) on
                            completion of graduation. That means you can work for a year after you finish your studies.</li>
                        <li>It is temporary employment permission allowing students the opportunity to gain practical
                            experience in their field of study.</li>
                        <li>After that, you’ll be required to apply for a work visa if you have to continue working in the
                            US. You can remain in the US up to 60 days after the completion of your course in US
                            universities even if you don’t have a job offer or haven’t applied for OPT</li>

                        <h3 class="sec-title pt-3 pb-3">Student Dependent Visa</h3>
                        <p>The student dependent visa is called the F2 Visa. The F2 visa is a non-immigrant dependent visa
                            where the immediate family members of the F1 student visa holders can come to the US. Dependents
                            include the spouse and unmarried children under the age of 21 of the individual who is studying
                            in US.</p>
                        <h3 class="sec-title pt-3 pb-3">Eligibility conditions for the F2 visa</h3>

                        <li>Must be the spouse of an F1 student visa holder.</li>
                        <li>Must be the dependent child (under 21 years and unmarried) of an F1 visa holder.</li>
                        <li>Applicant must have enough financial resources to support the family in the US</li>

                        <h3 class="sec-title pt-3 pb-3">F2 visa requirements</h3>

                        <li>Passport (both original and photocopies)</li>
                        <li>Visa application confirmation (DS-160)</li>
                        <li>A photograph conforming to U.S. visa rules</li>
                        <li>Birth certificate for dependent children</li>
                        <li>Marriage certificate for spouses</li>
                        <li>Visa fee payment receipt</li>
                        <li>Applicant’s I-20 form</li>
                        <li>Copy of F1 visa holder’s I-20 form</li>
                        <li>Applicant’s bank statements, tax records, and employment documents as proof of financial
                            stability</li>

                        <h3 class="sec-title pt-3 pb-3">Benefits Of F2 Visa</h3>
                        <p style="text-align: justify";><b>Extended visa stays</b></p>
                        <p style="text-align: justify";>If the primary F1 student visa holder extends his / her stay, then
                            the F2 visa dependents are also automatically eligible to apply for the extension. Filing Form
                            I-539 is enough to renew your F2 visa along with proof of your financial status.</p>
                        <p style="text-align: justify";><b>Change of visa status</b></p>
                        <p style="text-align: justify";>You can enter the U.S. on an F2 visa and subsequently request a
                            change of visa status. By enrolling in a US higher education program in US universities, you can
                            change your visa status to F1. If you find an appropriate job, you can request a change of visa
                            status.</p>
                        <p style="text-align: justify";><b>Getting a Green Card</b></p>
                        <p style="text-align: justify";>While you get a Green Card automatically when your primary F1 visa
                            holder receives one, you are also eligible to apply for one on your own. You can change your
                            visa status to a different one which allows for dual purpose (e.g., L1 visa) and then apply for
                            a Green Card. If you find employment, you become eligible for a Green Card.</p>
                        <p style="text-align: justify";><b>Access to healthcare</b></p>
                        <p style="text-align: justify";>F2 visa holders have access to medical services and hospitals in the
                            US. However, if you plan on a long stay or expect a medical situation, it makes sense to buy a
                            health insurance plan to cover the high costs of health care.</p>
                        <h3 class="sec-title pt-3 pb-3">F2 VISA RESTRICTIONS</h3>

                        <li>No permission to work</li>
                        <li>Not eligible for Social Security Number</li>
                        <li>Not allowed to pursue higher education</li>
                        <li>Cannot enter the U.S. before the F1 student visa holder</li>
                        <li>As you are not eligible to work, you can’t get a Social Security Number (SSN).</li>
                        <li>You can’t get employment in the United States on an F2 visa. But you’re allowed to do unpaid
                            voluntary work</li>
                        <li>You cannot do a Bachelor’s or Master’s degree program in US universites on the F2 visa. Yet you
                            are eligible for recreational and vocational training courses. Dependent children on an F2 visa
                            can complete primary, middle and high school education. If you are looking to pursue higher
                            studies, you must first apply for a visa status change.</li>
                        <li>You must either be accompanied by the primary F1 visa holder or fly later after the F1 holder
                            for first-time travel to the U.S. on an F2 visa. You can’t enter the United States before the F1
                            visa holder. This only applies when you are first entering the U.S. and not for subsequent
                            travel</li>

                        <h3 class="sec-title pt-3 pb-3">M1 VISA – STUDENT VISA (VOCATIONAL COURSE)</h3>
                        <p style="text-align: justify";>M1 visa is a type of non-immigrant student visa issued by the USCIS
                            to foreign students looking to study in the United States, but not every student receives an M1
                            visa. This is primarily intended for those who want to pursue vocational training in the U.S.
                        </p>
                        <p style="text-align: justify";>Students can enter the US with an M1 visa and complete their
                            full-time vocational studies.</p>
                        <h3 class="sec-title pt-3 pb-3">What can you do with an M1 visa</h3>
                        <p style="text-align: justify";>Using the M1 visa, as a student, you can obtain a driver’s license,
                            a bank account based in the U.S., access to health care services, and apply for work under
                            certain restrictions.</p>
                        <p style="text-align: justify";><b>What you cannot do-.</b></p>
                        <p style="text-align: justify";>Requirements for student visa application</p>

                        <li>You cannot apply for non-academic or vocational training courses in the US</li>
                        <li>You meet the minimum education qualification required for admission into the vocational course.
                        </li>
                        <li>You were admitted by a U.S.-based educational school and got the Form I-20.</li>
                        <li>You have the appropriate level of proficiency in English</li>
                        <li>You’ve shown you’ve got enough funds to cover your expenses while you’re in the US</li>
                        <li>You have permanent residence in your country of origin</li>
                        <li>You have no intention of remaining in the US and will leave after your education is complete
                        </li>
                        <li>The institution is convinced that the education you are receiving will benefit your country of
                            origin</li>


