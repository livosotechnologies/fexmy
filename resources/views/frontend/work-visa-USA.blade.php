                    <p style="text-align: justify;">L2 visas are also known as L1 dependent visas. The L2 visa is granted to the spouses and dependents of L1 visa holders. An L2 visa is a non-immigrant visa.</p>
                    <p style="text-align: justify;"><b>L1 dependents include:</b></p>
                    <p style="text-align: justify;">Spouses of visa holders</p>
