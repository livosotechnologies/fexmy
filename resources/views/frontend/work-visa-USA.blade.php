                    <li>Live in the U.S. temporarily</li>
                    <li>Study full-time or attend college in the U.S.</li>
                    <li>Apply for an Employment Authorization Document (EAD) to work in the U.S.</li>
                    <li>Change your visa status to another non-immigrant visa like F1, B1/B2, H1, and L1, and</li>
                    <li>Travel in and out of the US on brief international trips</li>

