                    <h3 class="sec-title pt-3 pb-3">L-1A Visa:</h3>
                    <p style="text-align: justify;">The L-1A Visa is a US-issued non-immigrant visa. The U.S. Citizenship and Immigration Services (USCIS) issues this visa for foreign directors or managers being transferred to U.S. offices of their company. International businesses that do not have associated offices in the U.S. may also use an L-1A visa to send a manager or executive to create one there.</p>
                    <p style="text-align: justify;">The L-1A visa is one of two categories of work visas in the L-1 visa category. Its equivalent, the L-1B visa is for staff with advanced skills who choose to migrate to the U.S. for a five-year period. Upon expiry, the applicant may again apply for the L-1 visa holder status only after working for the parent, subsidiary, branch or affiliate of the organization for at least one year outside the U.S.</p>
                    <p style="text-align: justify;"><b>L1 Visa Requirements:</b></p>
                    <p style="text-align: justify;">There are two main requirements for an L1 visa: employer requirements and employee requirements.</p>
                    <p style="text-align: justify;"><b>Employer Requirements:</b></p>
                    <p style="text-align: justify;">A qualifying relationship must be formed between the employer within the US and its international branch/subsidiary/affiliate that employs the applicant. This indicates that the two organizations are in some way tied by their ownership or association with each other.</p>
                    <p style="text-align: justify;"> Furthermore, the employer needs to be doing business in the US and at least one other country during the visa holder&#8217;s stay in the US.</p>
                    <p style="text-align: justify;"><b>Employee Requirements:</b></p>
                    <p style="text-align: justify;">With an L1 visa, the applicant must have been working for the international employer for at least one year in the preceding three years before they were admitted to the U.S.</p>
                    <p style="text-align: justify;">This employment must have been in an administrative, corporate, or technical area of expertise and their prospective jobs in the U.S. must be within the same fields.</p>
                    <p style="text-align: justify;"><b>L1A Visa to Green Card</b></p>
                    <p style="text-align: justify;">An L1A visa applicant has a relatively straightforward way of acquiring a green card since it falls under an EB-1C Green Card. This does not require a PERM Labor Certificate, cutting off the processing time by up to 8 months.</p>
                    <p style="text-align: justify;">The employer just needs to file an I-140 petition to qualify for the green card. If the applicant has been accepted, he files for a status adjustment (Form I-485).</p>
