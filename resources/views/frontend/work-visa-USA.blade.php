                    <h3 class="sec-title pt-3 pb-3">R1 Visa:</h3>
                    <p style="text-align: justify;">An R1 visa is for those who want to work in the United States as religious workers. It is a short-term visa.</p>
                    <p style="text-align: justify;"><b>Eligibility Conditions</b></p>
                    <p style="text-align: justify;">First of all, you will have to work at a part-time job in the US, meaning that you&#8217;ll spend at least 20 hours a week at work.</p>
                    <p style="text-align: justify;">At the same time, you need to get a job that has to do with your religion. To put it another way, you have to either work in a religious position or be a minister there. This means you &#8216;re not going to be suited to R1 Visas if your work is anything other than religious.</p>
                    <p style="text-align: justify;">You are also entitled if you have a bona fide religious charitable organization and you were a member of the religious community before applying for a visa and you spent at least two years in that role before applying for an R1 visa.</p>
                    <p style="text-align: justify;">You can also be a worker for an employer who also has a religious non-profit organization in the US.</p>
                    <p style="text-align: justify;">You are not eligible if you are not going to work in a religious group or a church or is not going to do anything related to their activities. These visas are particularly for religious workers who have a job in the area, and no one else.</p>
                    <p style="text-align: justify;">Being a volunteer also won&#8217;t bring an R1 Visa. You have to be a paid worker-otherwise you can&#8217;t apply.</p>
                    <p style="text-align: justify;"><b>Documents Required</b></p>

                    <li>A Nonimmigrant Visa Electronic Application (DS-160) Form</li>
                    <li>A passport valid for travel to the United States with a validity date at least six months beyond your intended period of stay.</li>
                    <li>Passport photo taken in the last six months.</li>

                    <h3 class="sec-title pt-3 pb-3" style="text-align: justify;">R2 Visa:</h3>
                    <p style="text-align: justify;">The R2 visa is a temporary US visa that authorizes an R1 visa holder&#8217;s spouse and unmarried child (under 21 years) to enter the US. The holders of R2 status may remain in the country for as long as the holders of R1 remain in legal status. In other words, once the principal R-1 person loses legal status, an individual loses his / her R-2 status.</p>
                    <p style="text-align: justify;"><b>R2 visa holders can:</b></p>

                    <li>Take part in a full-time study course in the US</li>
                    <li>Travel freely in and out of the US during the validity of the visa</li>
                    <li>Apply for permanent residency status</li>

                    <p style="text-align: justify;">However, the R2 visa holder cannot work in the US.</p>
                    <p style="text-align: justify;"><b>Documents Required</b></p>

                    <li style="text-align: justify;">All documents required for an R-1 visa.</li>
                    <li style="text-align: justify;">Original birth certificate for each child</li>
                    <li style="text-align: justify;">Marriage registration certificate</li>
                    <li style="text-align: justify;">Copy of spouse’s passport</li>
                    <li style="text-align: justify;">Proof of financial support</li>
                    <li style="text-align: justify;">Copy of the contract between the employer and the R1 religious worker</li>

