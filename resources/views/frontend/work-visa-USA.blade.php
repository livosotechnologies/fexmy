                    <h3 class="sec-title pt-3 pb-3">H4 Visa:</h3>
                    <p style="text-align: justify;">An H4 visa is a non-immigrant dependent visa. The visa does not grant you permanent residency, but it gives you the right to live, study, and work in the US.</p>
                    <p style="text-align: justify;"><b>Who is eligible?</b></p>

                    <li style="text-align: justify;">Spouse of the H type visa holder</li>
                    <li style="text-align: justify;">Children under the age of 21 whose parent is an H visa holder</li>

                    <p style="text-align: justify;"><b>The validity of the H4 visa</b></p>
                    <p style="text-align: justify;">The validity of the visa is dependent on the visa of the sponsor who is also called the principal applicant.</p>
                    <p style="text-align: justify;">The visa is usually sponsored by the spouse or parent having the H type visa. The H4 visa becomes invalid when the visa of the sponsor expires.</p>
                    <p style="text-align: justify;"><b> Privileges of the H4 visa</b></p>

                    <li>You can get a driver&#8217;s license</li>
                    <li>You have opportunities to study in the US</li>
                    <li>You qualify for financial services, such as banking and an H4 visa loan</li>

                    <p style="text-align: justify;"><b> Work permission for the H4 visa holder</b></p>

                    <li>The holder of the H4 visa may work part-time, full-time or not at all.</li>
                    <li>The H4 visa holder is permitted to start any form of business.</li>
                    <li>The holder of the H4 visa may continue to be eligible for EAD even if he does not seek employment.</li>

