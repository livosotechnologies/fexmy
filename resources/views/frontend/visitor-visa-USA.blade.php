                    <p style="text-align: justify;">With the D visa, the crew members can go through to the U.S and stay in the country for a maximum of 29 days.</p>
                    <p style="text-align: justify;">The D visa allows the crews to go through the U.S and stay for a maximum of 29 days. Holders of the D visa can leave the dock or airport for this duration but must also leave the country within 29 days. The D visa can only be used for the purpose of passing through the U.S.</p>
