                    <h3 class="sec-title pt-3 pb-3">Requirements for obtaining a work permit</h3>
                    <p style="text-align: justify;"><strong>Recognition of your qualifications by German authorities:</strong> when applying for positions in Germany, you must not only submit evidence of your professional and educational qualifications, but you must also have your professional skills recognized by German authorities. For regulated professions like doctors, nurses, and teachers, this is critical. For this aim, the German government runs a website.</p>
                    <p style="text-align: justify;"><strong>Knowledge of German:</strong> If you have some knowledge of the language, you will have an advantage over other job seekers. You will have a good chance of finding job here if you have the necessary school qualifications, work experience, and a basic grasp of German (B2 or C1). For expert occupations such as research and development, however, knowledge of German is not required.</p>
                    <h5>Language prerequisites</h5>
                    <p style="text-align: justify;">The good news is that IELTS isn&#8217;t required for employment in Germany.</p>
                    <p style="text-align: justify;">The English language requirements, on the other hand, may vary depending on the work. If your job needs you to travel to other countries, you will need to be fluent in English.</p>
                    <p style="text-align: justify;">A basic understanding of German, on the other hand, will increase your work prospects.</p>
                    <h3 class="sec-title pt-3 pb-3">Work visa for non-EU residents</h3>
                    <p style="text-align: justify;">Non- EU residents must apply for a work visa and a residence permit before travelling to Germany. They must approach the German embassy or consulate in their country. Their application must include the following:</p>

                    <li style="text-align: justify;">Job offer letter from the firm in Germany</li>
                    <li style="text-align: justify;">Valid passport</li>
                    <li style="text-align: justify;">Annexure for an employment permit</li>
                    <li style="text-align: justify;">Certificates of academic qualification</li>
                    <li style="text-align: justify;">Certificates of work experience</li>
                    <li style="text-align: justify;">Approval letter from the Federal Employment Agency</li>

                    <h3 class="sec-title pt-3 pb-3">EU Blue card</h3>
                    <p style="text-align: justify;">Individuals are eligible for the EU Blue card if they have a graduate or undergraduate degree from a recognized university and a job with an annual gross salary of 52,000 Euros (as of 2018) in Germany before moving there.</p>
                    <p style="text-align: justify;">Individuals who have graduated from a German university or are a highly skilled professional in the field of either mathematics, IT, life sciences or engineering or are a medical professional are also eligible. The conditions are you must earn a salary comparable to German workers.</p>
                    <p style="text-align: justify;"><b>Privileges of EU Blue Card:</b></p>

                    <li>Allowed to stay in Germany for four years</li>
                    <li>Eligible for permanent residency after two or three years</li>
                    <li>Spouse and children are eligible to come with you</li>
                    <li>Family members eligible for work permit</li>

                    <h3 class="sec-title pt-3 pb-3">Bringing your family on a work visa</h3>
                    <p style="text-align: justify;">You must meet the following requirements if you wish to bring your family to Germany with you:</p>
                    <p style="text-align: justify;">Your children must be under the age of eighteen.</p>
                    <p style="text-align: justify;">Your salary must be sufficient to support you and your family.</p>
                    <p style="text-align: justify;">You must be able to provide for your family&#8217;s needs.</p>
