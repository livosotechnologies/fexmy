                    <h3 class="sec-title pt-3 pb-3">UK Skilled Worker Visa Details</h3>
                    <p style="text-align: justify" ;>The Skilled Worker visa allows skilled professionals to build their professional career in the UK. The duration for stay as per the U.K. Skilled Worker is for a maximum period of 5 years The Skilled Worker Visa is a points-based visa and applicants must score a minimum of 70 points for their application to be considered. The points are awarded based on: </p>

                    <li>Whether you have a Certificate of Sponsorship from an employer</li>
                    <li>Whether you are receiving an appropriate salary</li>
                    <li>Your English communication skills</li>
                    <li>The maintenance funds you hold</li>

                    <p style="text-align: justify" ;>If you satisfy these parameters you can then apply for the Skilled Worker Visa.</p>
                    <p style="text-align: justify" ;>The Tier 2 visa is subdivided to allow for the following professionals: </p>

                    <li>Tier 2 General Visa: For workers who have a job offer in the UK and whose profession appears on the shortage occupation list. <strong>This has been replaced by the Skilled Worker visa</strong></li>
                    <li>Tier 2 Intra-Company Transfer Visa: For workers of corporations transferring to the UK </li>
                    <li>Tier 2 Minister of Religion Visa: For ministers of religions within a religious organisation </li>
                    <li>Tier 2 Sportsperson Visa: For coaches and sportspersons </li>

                    <p style="text-align: justify" ;>If you have successfully received a Skilled Worker visa you can: </p>

                    <li>Study in the UK</li>
                    <li>Bring family members to the UK</li>
                    <li>Travel to and from the UK</li>

                    <h3 class="sec-title pt-3 pb-3">Processing time</h3>
                    <p style="text-align: justify" ;>You can apply for this visa three months before you begin working in the UK. The starting date will be mentioned in the certificate of sponsorship you will receive from your UK employer.</p>
                    <p style="text-align: justify" ;>You will get the decision on your visa within three weeks of your application. With the UK government including more occupations in the Shortage Occupation List, the processing time is expected to be reduced for many applicants.</p>
                    <h3 class="sec-title">HOW LONG CAN ONE STAY ON SKILLED WORKER VISA?</h3>
                    <p style="text-align: justify" ;>You can stay for a maximum of 5 years on this visa. </p>
                    <p style="text-align: justify" ;>The duration of work visa depends on the length of your job contract. If you have not exceeded the maximum duration for your visa type, you can extend your stay. You will need to apply for an extension, either online or at a premium service center for UK visas.</p>
                    <p style="text-align: justify" ;>You can stay for a maximum of 5 years and 14 days on the Tier 2 visa or the duration mentioned on your certificate of sponsorship (plus 1 month) whichever duration is shorter.</p>
                    <h3 class="sec-title pt-3 pb-3">Documents Required</h3>
                    <p style="text-align: justify" ;>The documentation required for the UK Tier 2 Visa includes:</p>

                    <li>Having a valid Certificate of Sponsorship from an employer</li>
                    <li>Salary and financial details</li>
                    <li>Current passport and travel history</li>
                    <li>Certifications proving your English skills</li>
                    <li>Criminal record certificate</li>
                    <li>Other supporting documents</li>

                    <h3 class="sec-title pt-3 pb-3">SKILLED WORKER DEPENDENT VISA</h3>
                    <p style="text-align: justify" ;>The Skilled Worker dependent visa is for children and partners of those who have come to the country on a Skilled Worker visa or have applied for one. </p>
                    <p style="text-align: justify" ;><strong>The following persons qualify for a Skilled Worker dependent visa:</strong> </p>
                    <p style="text-align: justify" ;>Spouse </p>
                    <p style="text-align: justify" ;>Unmarried or same-sex partner </p>
                    <p style="text-align: justify" ;>Children under the age of 18 at the time of application </p>
                    <p style="text-align: justify" ;>Children over the age of 18 who are dependents </p>
                    <p style="text-align: justify" ;>The partnership between spouses and partners should be genuine and they should plan to live together for the duration of stay in the country. </p>
                    <p style="text-align: justify" ;>Maintenance funds: </p>
                    <p style="text-align: justify" ;>Skilled Worker dependents have no recourse to public funds as such, in their application they must prove access to adequate financial means for the period of stay in the UK and if there are dependents, they must demonstrate an additional £ 630 available for each dependent. </p>
                    <p style="text-align: justify" ;>Age: </p>
                    <p style="text-align: justify" ;>The principal applicant and dependent must be at least 18 on the date of arrival in the United Kingdom, or when the visa is issued. </p>
                    <p style="text-align: justify" ;>Other requirements: </p>
                    <p style="text-align: justify" ;>You must not be a previous holder of a UK visa for students, short-term study visa on or after April 2015 or have been given leave on or after April 2015 as a parent of a Tier 4 student (child). </p>
                    <p style="text-align: justify" ;>Furthermore, you will have to qualify the general grounds for admission. You must have a clear history of immigration, with no case of overstaying. When your spouse or relative’s visa expires, you must have no intention to stay in the UK. </p>
                    <p style="text-align: justify" ;>Application process: </p>
                    <p style="text-align: justify" ;> Skilled Worker dependent visa applications may be made with the main Skilled Worker visa application either simultaneously or later. </p>
                    <p style="text-align: justify" ;>Regardless of when the applications are submitted, successful applicants will be given leave in accordance with the time of leave of the principal visa applicant. </p>
                    <p style="text-align: justify" ;>The approval process can vary according to where the approval for a Tier 2 based visa is made. </p>
                    <p style="text-align: justify" ;>As a holder of Skilled Worker dependent visas, you can: </p>
                    <p style="text-align: justify" ;>Remain in the UK for the same period as the main Skilled Worker visa holder </p>
                    <p style="text-align: justify" ;>Work, with limited exceptions </p>
                    <p style="text-align: justify" ;>Study or take a postgraduate course, under certain conditions </p>
                    <p style="text-align: justify" ;>Apply to extend your visa in compliance with the principal applicant, provided that you continue to meet the eligibility conditions. When the key visa holder has left the UK, you will not be eligible to apply for an extension. </p>
                    <p style="text-align: justify" ;>You cannot access public funds, or work as a doctor in training, or as a dentist, or as a sports instructor for professionals </p>
                    <p style="text-align: justify" ;>Tier 2 visa applications are evaluated on the basis of the UK’s point-based system. One must have at least 70 points to qualify for the visa. You can score 30 points with a job offer with an employer sponsorship certificate. You can score another 30 points if your occupation finds a place in the Skills Shortage list. With these 60 points it will be relatively easy to get the remaining points to qualify.</p>
                    <p style="text-align: justify" ;>Finding a UK employer who can sponsor a Tier 2 visa</p>
                    <p style="text-align: justify" ;>It will be easy to find one in the ‘Register of Sponsors Licensed Under the Points-based System’ which is available to the public. This contains a list of all the employers who have the permission to sponsor international employees.</p>
