                    <h3 class="sec-title pb-3">AUSTRALIA TEMPORARY SKILLS SHORTAGE (TSS) VISA (SUB CLASS 482)</h3>

                    <p style="text-align: justify;">This visa allows a skilled worker travel into Australia to work in his/her nominated occupation for that person’s approved sponsor (employer) for up to four years.</p>
                    <p style="text-align: justify;">Before an employee can apply for a Subclass 482 visa, he /she should have an employer who is a standard business sponsor and also must have applied for a nomination with the Department of Home Affairs(DHA) for the sponsoring applicant.</p>
                    <p style="text-align: justify;">Employers who already known (standard business sponsor) can file for the nomination of the employee and once the nomination is approved, the applicant will have to lodge the visa application within 6 months.</p>
                    <p style="text-align: justify;">Employers, who are not eligible sponsors, should apply to become one first and then file for employee nominations. Sponsorship and Nomination applications can also be made simultaneously.</p>
                    <p style="text-align: justify;">There are many obligations for an employer to become a business sponsor and to nominate an employee. Employer is required to meet the requirements set by the department of immigration for an eligible sponsor based on the business tenure, critical requirement of positions, training benchmarks, if they have checked that there are no Australian citizens /PR holders available to occupy these positions, the salary being offered to the nominating employee and many other requirements.</p>

                    <br>
                    <h3 class="sec-title pt-3 pb-3">THE VARIOUS STREAMS UNDER 482 SUB CLASS VISA</h3>
                    <li>The Short-Term stream – 2 – 4 years, occupation should be in STSOL</li>
                    <li>The Medium-Term stream – Upto 4 years, occupation should be in MLTSSL</li>
                    <li>The Labour Agreement stream – Upto 4 year, as per the Labour Agreement</li>
                    <br>

                    <h3 class="sec-title pt-3 pb-3">ELIGIBILITY FOR TEMPORARY SKILLS SHORTAGE VISA (TSS VISA)</h3>
                    <li>Have been sponsored by an approved standard business sponsor</li>
                    <li>Nominated under a skilled occupation that has been approved by the Australian Government</li>
                    <li>Have the required skills to fill a position nominated by an approved standard business sponsor</li>
                    <li>English requirements, Registration /License(if applicable)</li>
                    <li>Eligible to work only in the nominated occupation</li>
                    <li>Satisfy health, character and other skills requirements</li>
                    <li>Have adequate health insurance unless you are covered by Medicare</li>
                    <li>Can include your partner, dependent children and family members</li>
                    <br>
                    <h3 class="sec-title pt-3 pb-3">APPLICATION COST:</h3>
                    <li>Eligible sponsor (Standard business sponsor) application fee (for employer): AUD420</li>
                    <li>Nomination application fee (for employer): AUD330</li>
                    <li>Visa Application fee for Temporary Skills Shortage visa (subclass 482 Short Term Stream) is AUD1,330 &amp; for Medium -Term &amp; Labour Agreement Stream – AUD2,770 same fee will be applicable for any additional applicant above 18 years and extra cost will be there for any additional applicant below 18 years &amp; it depends upon the Stream which you are applying</li>

