                    </li>
                    <li>Skilled – Recognized Graduate Visa (subclass 476): With this visa, recent engineering graduates can work, live, or study in Australia for up to 18 months. Applicants should have completed an engineering course from a specific institution within the past 2 years. The applicant should be under 31 years of age.</li>
                    <li>Graduate Temporary (Subclass 485) Visa: A visa for migrant students who have held a student visa in the last 6 months.</li>
