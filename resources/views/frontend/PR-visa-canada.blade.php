                    <h3 class="sec-title pt-3 pb-3">STEPS TO APPLY FOR THE EXPRESS ENTRY PROGRAM</h3>
                    <b>Step 1: Complete your ECA</b>
                    <p style="text-align: justify" ;>If you have done your education outside Canada, you should get your Educational Credentials Assessment or ECA. The ECA proves that your educational qualifications are equal to those recognized in the Canadian educational system.</p>
                    <b>Step 2: Complete your language ability tests</b>
                    <p style="text-align: justify" ;>The next step is completing the required English language proficiency tests. The minimum score is 6 bands in IELTS equivalent to CLB 7. Your test score should be less than 2 years old at the time of application.</p>
                    <p style="text-align: justify" ;>You will have an edge over other applicants if you know French. French language test like the Test de evaluation de Francians (TEF) will prove your proficiency in the language.</p>
                    <b>Step 3: Create your Express Entry profile</b>
                    <p style="text-align: justify" ;>You will have to first create your online Express Entry profile. The profile should include details about your age, work experience, education, language skills, etc. You will be given a score base on these details.</p>
                    <p style="text-align: justify" ;>If you qualify by getting the required points, you can submit your profile. This will be included in the Express Entry pool.</p>
                    <b> Step 4: Calculate your CRS score</b>
                    <p style="text-align: justify" ;>If your profile makes it to the Express Entry pool, it is ranked based on the Comprehensive Ranking System (CRS) score. Criteria such as age, work experience, adaptability, etc. determine your CRS score. Your profile gets included in the Express Entry pool if you have the required CRS score.</p>
                    <b> Step 5: Get your Invitation to Apply (ITA)</b>
                    <p style="text-align: justify" ;>If your profile gets selected from the Express Entry pool, you will get an ITA from the Canadian government after which you can start the documentation for your PR visa.</p>
