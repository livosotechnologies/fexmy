                        <b>Steps to study abroad in Canada:</b>
                        <b>Step1:</b>
                        <b>Research Your Options:</b>
                        <p style="text-align: justify" ;>Before you shortlist the universities, decide what you want to study, where you want to live, overall cost, various opportunities etc.</p>
                        <b>Step 2:</b>
                        <b>Select Universities:</b>
                        <p style="text-align: justify" ;>Shortlist the universities (around 10) & study programs that meet your requirements and where you want to apply for.</p>
                        <b>Step 3:</b>
                        <b>Prepare for entrance exams:</b>
                        <p style="text-align: justify" ;>Prepare for the standardized tests like TOEFL/GRE/GMAT/IELTS etc. based on the requirements of the universities & colleges. Register for these tests in advance. While applying for the tests you should also plan for the time required in case you need to retake the test. You should complete these tests before September of the year you wish to apply.</p>
                        <b>Step 4:</b>
                        <b>Finance your studies:</b>
                        <p style="text-align: justify" ;>Ensure that you have the money ready for your entire period of study in Canada along with accommodation, local travel, food, and other miscellaneous expenses. Decide on how you will finance your studies – personal saving, education loans, student scholarships or assistantship.</p>
                        <b>Step 5:</b>
                        <b>Apply to university:</b>
                        <p style="text-align: justify" ;>Contact each university directly for the admission requirements. Every university has its own admission requirements. Complete the applications well before deadlines and send them.</p>
                        <b>Step 6:</b>
                        <b>Confirm admission:</b>
                        <p style="text-align: justify" ;>Once you get your acceptance letters from the universities that you have applied to, choose the university you would like to study at. The next step is to pay a non-refundable deposit to the university to confirm your admission to study in Canada.</p>
                        <b>Step 7:</b>
                        <b>Get Student Visa:</b>
                        <p style="text-align: justify" ;>Once you get admission confirmation done, you can apply for your student visa.</p>
                        <h3 class="sec-title pt-3 pb-3">STUDENT VISA AND DEPENDENTS:</h3>
                        <p style="text-align: justify" ;>A foreign national on a Canada student visa in Canada can bring their spouse and dependent children to Canada. The spouse can also apply for an open work permit and even work as a Canadian employer for the time the study visa of their partner is valid. However, if the spouse wants to study in Canada, they will have to apply for a separate study visa.</p>
                        <p style="text-align: justify" ;>Student visa holders can also bring their dependent children to Canada. The children can stay for the duration till their study visa is valid inCanada. Minor children can pursue their school education in the country provided that their parents are either working or studying. The child must be below the age of 22. Children above 22 years who are not financially independent due to a physical or mental condition are also considered as dependents.</p>
