                        <h3 class="sec-title pt-3 pb-3">UPCOMING INTAKES IN THE CANADA</h3>
                        <p style="text-align: justify" ;>Canada universities and colleges offer three intakes. They are also be referred as semesters in some institutions. The three intakes available in Canada are:</p>
                        <li>Intake 1: Fall Semester – A popular intake which starts in the month of September</li>
                        <li>Intake 2: Winter Semester – Which starts in the month of January</li>
                        <li>Intake 3: Summer Semester – Usually starts from April/May, this intake is available for limited programs and colleges.</li>
                        <p style="text-align: justify" ;>We would advise you to apply well in advance because admissions and scholarships get difficult when you apply closer to the deadline. It is always feasible to apply 6 to 9 months before the academic session starts.</p>
                        <h3 class="sec-title">WORK AUTHORIZATION FOR STUDENTS:</h3>
                        <b>Student Applicant:</b>
                        <li>Students must be over 18 years of age.</li>
                        <li>International students in Canada universities can work on-campus or off-campus for up to 20 hours during university semesters and full-time during breaks such as the winter or summer holidays, without the need for a work permit.</li>
                        <li>Foreign students who are studying in a program that requires work experience such as through a co-op or internship program.</li>
                        <b>Spouse:</b>
                        <p style="text-align: justify" ;>In general, spouses are given the same rights as students who are already in Canada. Therefore, if the student with a Canada student visa has the right to work, the spouse who comes to join him or her will also have that right.</p>
                        <h3 class="sec-title pt-3 pb-3">STUDENT VISA REQUIREMENTS:</h3>

                        <p style="text-align: justify" ;>You will generally need the following for your CANADA Student Visa application:</p>
                        <li>Valid Passport</li>
                        <li>Attested copies of 10th, 12th and Degree certificates</li>
