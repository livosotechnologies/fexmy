                        <h3 class="sec-title pt-3 pb-3">AUSTRALIA STUDENT VISA REQUIREMENTS:</h3>

                        <p><strong>Student visa</strong><strong> details:</strong></p>
                        <p>The Australia Student Visa is called Subclass 500.</p>
                        <p>You will be eligible for a student visa only if you want to study a registered course or part
                            of it on a full-time basis.</p>
                        <p>The maximum validity of the student visa is five years.</p>
                        <p>The course you wish to pursue must be registered with the Commonwealth Register of
                            Institutions and Courses for Overseas Students (CRICOS).</p>

                        <li>Issued an electronic Confirmation of Enrolment (eCoE) certificate – this is to confirm your
                            enrolment in an Australian University.</li>
                        <li>Genuine Temporary Entrant (GTE) statement – this is proof of your intention to come to
                            Australia only to study and not to settle down here.</li>
                        <li>Four recent passport-sized photographs</li>
                        <li>Certified or notarized copies transcript/document of academic results</li>
                        <li>Overseas Student Health Cover (OSHC) – approved by the Australian government, this health
                            insurance provides basic medical and hospital cover. You can purchase this insurance through
                            your university.</li>
                        <li>Results of tests in the English language like IELTS, TOEFL, PTE if you are a non-native
                            English speaker</li>
                        <li>Evidence of monetary means for covering all expenses during the period of study</li>
                        <li>If applicable, proof of civil status</li>
                        <li>The Australian University will intimate of additional requirements if any prior to your
                            application</li>
                        <li>Financial Requirements – to get your student visa you must prove that you have the funds to
                            cover your course fees, travel, and living costs.</li>
                        <li>Character Requirement – you must submit a certificate to prove you have no criminal record.
                        </li>
                        <li>Proof of visa fees payment – proof that you have paid the required visa fees.</li>

                        <p>If there are any other additional requirements, the university you have been selected for
                            will let you know about them.</p>

