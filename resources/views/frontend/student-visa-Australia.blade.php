                        <p>Australian universities have diverse deadlines for application. However, the two general
                            timelines are broadly applicable:</p>
                        <p><strong>Intake 1:</strong> Semester 1 – this intake commences in February and is the major
                            intake for students.</p>
                        <p><strong>Intake 2:</strong> Semester 2 – this intake commences in July.</p>
                        <h3 class="sec-title pt-3 pb-3">WORK AUTHORIZATION FOR STUDENTS:</h3>
                        <p><strong>Student Applicant:</strong></p>

                        <li>Students must be over 18 years of age.</li>
                        <li>International students in Australia staying on a valid student visa can work for up to 20
                            hours per week.</li>
                        <li>An exception is, working as an academic assistant. There is no limit on the number of days
                            academic assistants may work.</li>
                        <li>They are not allowed to be self-employed or work as freelancers.</li>

                        <p>After graduation students can apply for the Temporary Graduate visa (subclass 485). The visa
                            will be granted based on the coursework and the category under which the student has applied
                            to study in Australia.</p>

                        <h3 class="sec-title pt-3 pb-3">AUSTRALIA STUDENT VISA REQUIREMENTS:</h3>

                        <p><strong>Student visa</strong><strong> details:</strong></p>
                        <p>The Australia Student Visa is called Subclass 500.</p>
                        <p>You will be eligible for a student visa only if you want to study a registered course or part
                            of it on a full-time basis.</p>
                        <p>The maximum validity of the student visa is five years.</p>
                        <p>The course you wish to pursue must be registered with the Commonwealth Register of
                            Institutions and Courses for Overseas Students (CRICOS).</p>

                        <li>Issued an electronic Confirmation of Enrolment (eCoE) certificate – this is to confirm your
                            enrolment in an Australian University.</li>
                        <li>Genuine Temporary Entrant (GTE) statement – this is proof of your intention to come to
                            Australia only to study and not to settle down here.</li>
                        <li>Four recent passport-sized photographs</li>
                        <li>Certified or notarized copies transcript/document of academic results</li>
                        <li>Overseas Student Health Cover (OSHC) – approved by the Australian government, this health
                            insurance provides basic medical and hospital cover. You can purchase this insurance through
                            your university.</li>
                        <li>Results of tests in the English language like IELTS, TOEFL, PTE if you are a non-native
                            English speaker</li>
                        <li>Evidence of monetary means for covering all expenses during the period of study</li>
                        <li>If applicable, proof of civil status</li>
                        <li>The Australian University will intimate of additional requirements if any prior to your
                            application</li>
                        <li>Financial Requirements – to get your student visa you must prove that you have the funds to
                            cover your course fees, travel, and living costs.</li>
                        <li>Character Requirement – you must submit a certificate to prove you have no criminal record.
                        </li>
                        <li>Proof of visa fees payment – proof that you have paid the required visa fees.</li>

                        <p>If there are any other additional requirements, the university you have been selected for
                            will let you know about them.</p>

                        <h3 class="sec-title pt-3 pb-3">AFTER YOU GRADUATE:</h3>

                        <li>If you completed a Bachelors, Masters or Doctoral degree, you may be eligible for the
                            Post-Study Work stream of the Temporary Graduate (subclass 485) visa once you finish your
                            course of study in Australia.</li>
                        <li>Graduate Work stream: International students with an eligible qualification who graduated
                            with skills and qualifications that relate to an occupation on the Medium and Long-term
                            Strategic Skills List (MLTSSL). A visa in this stream is granted for 18 months from the date
                            of .</li>
                        <br>
                        <h3 class="sec-title">POST-STUDY WORK OPTIONS:</h3>
                        <p><strong>Post-Study Work stream of the Temporary Graduate (subclass 485) </strong>visa grants
                            post-study work rights to international students who have completed a Bachelor’s, Masters,
                            or Doctoral degree in Australia on a student visa. They can work in the country for two to
                            four years to gain international work experience .</p>
                        <p>Students can also work under the<strong> Graduate Work Stream.</strong> They are eligible for
                            this stream if they have graduated with the skills and occupation that is related to an
                            occupation which features in the Medium and Long-term Strategic Skills List (MLTSSL). This
                            visa is valid for 18 months.</p>


