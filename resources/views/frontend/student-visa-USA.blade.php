                    </p>
                    <p style="text-align: justify" ;>
                        Intake 2: Term 2 – It commences in January/February intake is also available
                    </p>
                    <p style="text-align: justify" ;>
                        Intake 3: Term 3 – It commences in May/June and is available for selected Courses.
                    </p>

                    <h3 class="sec-title pt-3 pb-3">WORK AUTHORIZATION FOR STUDENTS:</h3>
                    <h3 class="sec-title pt-3 pb-3">Student Applicant:</h3>
                    <li>
                        Students must be over 18 years of age
                    </li>
                    <li>
                        International students who study a full-time undergraduate or postgraduate degree course at a recognised university on a Student Visa are allowed to work part-time during term for up to 20 hours a week and full-time during the holidays.
                    </li>
                    <li>

                        Each higher education institution has clearly defined semesters throughout the academic year, including specific breaks for holidays. During these breaks, you can work full-time at your job if you wish.
                    </li>
                    <h3 class="sec-title pt-3 pb-3">Spouse:</h3>
                    <p style="text-align: justify" ;>
                        Dependent Visa holders can work freely in the UK however they are not allowed to work as a doctor in training. If your dependent spouse is highly skilled and secures sponsorship with a Tier 2 licensed employer on his or her own account please note that they will not be able to ‘switch’ in country to a Tier 2 General Visa.
                    </p>
                    <h3 class="sec-title pt-3 pb-3">UK Student Visa Requirements:</h3>
                    <li>
                        A valid passport or any other documentation for travel
                    </li>
                    <li>
                        Proof of funds to cater to your living costs during your course
                    </li>
                    <li>
                        Funds need to be maintained for 28 days (tuition fees + living cost)
                    </li>
                    <li>
                        CAS reference number – Confirmation of Acceptance for Studies
                    </li>
                    <li>
                        Documents used for obtaining the CAS
                    </li>
                    <li>
                        Color photographs of passport size
                    </li>
                    <li>
                        If needed, screening for Tuberculosis
                    </li>
                    <li>
                        Your University will make you aware of additional requirements if any prior to your application
                    </li>
                    <b>
                        Post study work options
                    </b>
                    <li>
                        International students in the UK on a valid Tier 4 visa are allowed to stay in the country after finishing their education if they have a job offer worth at least GBP 20,800 per year.
                    </li>
                    <li>
                        To stay in the UK, such students can move from a Tier 4 Visa to a Tier 2 General Visa with a five-year validity period.
                    </li>
                    <li>
                        Students’ post-study work experience will assist them apply for permanent residence in the United Kingdom.
                    </li>
                    <h3 class="sec-title pt-3 pb-3"> BRINGING DEPENDENT FAMILY MEMBERS TO THE UK:</h3>
                    <p style="text-align: justify" ;>
                        On the dependent visa, your family members including your spouse and dependents will be able to join you or stay with you while you study in UK.
                    </p>
                    <b>
                        Definition of dependent:
                    </b>
                    <li>
                        Husband, wife or civil partner
                    </li>
                    <li>
                        Same-gender partner
                    </li>
                    <li>
                        Children under the age of 18 and children born in the UK during your Stay
                    </li>
                    <b>
                        You will be eligible to bring a dependent provided you are:
                    </b>
                    <li>
                        Sponsored by a higher education institute for a Level 7 course with a duration of 9 months or more
                    </li>
                    <li>
                        Doing a government-sponsored course that has more than 6 months duration
                    </li>
                    <li>
                        Doctorate students on an extension scheme
                    </li>
                    <h3 class="sec-title pt-3 pb-3">UK STUDENT DEPENDENT VISA:</h3>
                    <h3 class="sec-title pt-3 pb-3"> When to apply for a dependent visa</h3>
                    <p style="text-align: justify" ;> A dependent can apply at the same time as the UK Student Visa applicant or after the student has obtained the result of their application for a visa.
                    </p>

                    <p style="text-align: justify" ;>When the applicant has applied for further leave to stay in the UK after their study in UK and has not yet obtained a ruling, family members will have to wait until his permit is approved before submitting their permit for entry clearance.
                    </p>
                    <h3 class="sec-title pt-3 pb-3">Where to apply</h3>
                    <p style="text-align: justify" ;>Like for a Student Visa application, dependents should apply for nationality or country of residence in their country. The application will be filed online. For each dependent, a separate application must be completed.
                    </p>
                    <p style="text-align: justify" ;> In most countries, an appointment to attend the visa application center for biometrics enrolment and submission of supporting documents is booked as part of the online applications.
                    </p>
                    <h3 class="sec-title pt-3 pb-3">Financial requirements</h3>
                    <p style="text-align: justify" ;>Dependents who apply for a visa must have ample funds to meet the cost of living in the UK. It is £680 per month for which the visa is valid, up to a maximum of 9 months. That is a total of £6,120 per dependent if the visa is granted for a period of 9 months or more (9 x £680).
                    </p>
                    <p style="text-align: justify" ;> A bank statement or bank letter can be required to satisfy this requirement, and the funds must be kept in the name of the student or the name of the spouse/partner. If the UK Student Visa holder has an official sponsor that covers the living expenses of any dependent, a letter from the sponsor stating this may be submitted to meet the financial criteria.
                    </p>
                    <h3 class="sec-title pt-3 pb-3">AFTER YOU GRADUATE:</h3>
